import Foundation

func sum(value1: Int, value2: Int) -> Int {
    let result = value1 + value2
    return result
}

func menos(value1: Int, value2: Int) -> Int {
    let result = value1 - value2
    return result
}

func multiplicacao(value1: Int, value2: Int) -> Int {
    let result = value1 * value2
    return result
}

func divisao(value1: Int, value2: Int) -> Int {
    let result = value1 / value2
    return result
}
